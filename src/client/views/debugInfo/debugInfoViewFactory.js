this.DebugInfoViewFactory = {

	debugInfoView: _debugInfoView

};

// -----------------------------------------------------------------------------------------------------------------
// FUNCTIONS
// -----------------------------------------------------------------------------------------------------------------

	function _debugInfoView() {

		var debugInfo = new library.meteor.core.Surface({
            template: Template.debugInfo,
            size: [undefined, undefined],
            properties: {
                backgroundColor: 'lightgrey'
            }
        });

        return debugInfo;
	}

