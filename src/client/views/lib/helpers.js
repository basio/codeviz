Helpers = {
    UI: {
        focusText: _focusText
    }
};

// ---------------------------------------------------------------------------------------------------------------------
// HELPER FUNCTIONS: UI
// ---------------------------------------------------------------------------------------------------------------------

//This function puts our cursor where it needs to be.
//e.g: Helper.UI.focusText( t.find("#txtAddDocItem") );

function _focusText(i, val) {
    i.focus();
    i.value = val ? val : "";
    i.select();
}

