Meteor.methods({

	executeCode: function(id) {
		if (Meteor.isClient) { console.log('client: executeCode'); }

		if (Meteor.isServer) {
			console.log('server: calling _executeCode');
			_executeCode(id);
		}
	}

});//Meteor.methods


